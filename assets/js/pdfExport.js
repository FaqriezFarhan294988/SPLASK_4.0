/*=============== SPLASK PDF EXPORT ===============*/
const SPLaSKPDF = (() => {

    /* ── colour palette (matches dashboard theme) ── */
    const COLORS = {
        primary:    [46, 125, 96],
        primaryAlt: [36, 100, 77],
        dark:       [33, 37, 41],
        muted:      [108, 117, 125],
        light:      [248, 249, 250],
        white:      [255, 255, 255],
        green:      [25, 135, 84],
        yellow:     [255, 193, 7],
        red:        [220, 53, 69],
        watermark:  [46, 125, 96]
    }

    const PAGE_W = 210
    const PAGE_H = 297
    const MARGIN = 20
    const CONTENT_W = PAGE_W - MARGIN * 2

    /* ── serial number generator ── */
    function generateSerial(){
        const now  = new Date()
        const year = now.getFullYear()
        const seq  = String(Math.floor(Math.random() * 999999) + 1).padStart(6, '0')
        return `REPORT-${year}-${seq}`
    }

    /* ── score → colour ── */
    function scoreColor(score){
        if(score > 80) return COLORS.green
        if(score >= 50) return COLORS.yellow
        return COLORS.red
    }

    /* ── draw watermark on current page ── */
    function drawWatermark(doc){
        doc.setFontSize(54)
        doc.setTextColor(46, 125, 96)

        const gState = doc.GState ? new doc.GState({ opacity: 0.04 }) : null
        if(gState){
            doc.saveGraphicsState()
            doc.setGState(gState)
        }

        const text = 'SPLaSK'
        for(let y = 60; y < PAGE_H; y += 80){
            for(let x = 10; x < PAGE_W; x += 90){
                doc.text(text, x, y, { angle: 35 })
            }
        }

        if(gState){
            doc.restoreGraphicsState()
        }
    }

    /* ── ensure space: add page if needed ── */
    function ensureSpace(doc, y, needed){
        if(y + needed > PAGE_H - 25){
            doc.addPage()
            drawWatermark(doc)
            drawFooter(doc, generateSerial())
            return 30
        }
        return y
    }

    /* ── section divider ── */
    function drawDivider(doc, y){
        doc.setDrawColor(...COLORS.primary)
        doc.setLineWidth(0.5)
        doc.line(MARGIN, y, PAGE_W - MARGIN, y)
        return y + 6
    }

    /* ── section heading ── */
    function drawSectionTitle(doc, y, title){
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(13)
        doc.setTextColor(...COLORS.primary)
        doc.text(title, MARGIN, y)
        return y + 8
    }

    /* ── page footer ── */
    function drawFooter(doc, serial){
        const pageCount = doc.internal.getNumberOfPages()
        for(let i = 1; i <= pageCount; i++){
            doc.setPage(i)
            doc.setFontSize(8)
            doc.setTextColor(...COLORS.muted)
            doc.text(`Page ${i} of ${pageCount}`, PAGE_W / 2, PAGE_H - 10, { align: 'center' })
            doc.text('Generated by SPLaSK — Website Compliance Scanner', MARGIN, PAGE_H - 10)
            doc.text(serial, PAGE_W - MARGIN, PAGE_H - 10, { align: 'right' })
        }
    }

    /* ── draw a progress bar ── */
    function drawProgressBar(doc, x, y, width, height, score){
        const color = scoreColor(score)
        // background track
        doc.setFillColor(230, 230, 230)
        doc.roundedRect(x, y, width, height, 2, 2, 'F')
        // filled portion
        const fillW = Math.max((score / 100) * width, 0)
        doc.setFillColor(...color)
        doc.roundedRect(x, y, fillW, height, 2, 2, 'F')
    }

    /* ══════════════════════════════════════════════
       MAIN: generateReport(data)
       data = {
         url, totalScore, scanDate,
         categories: [{ name, score, status, explanation,
                        subCategories: [{ name, status, explanation }] }]
       }
       ══════════════════════════════════════════════ */
    function generateReport(data){
        if(!window.jspdf || !window.jspdf.jsPDF){
            alert('PDF library failed to load. Please check your internet connection and refresh.')
            return null
        }
        const jsPDF = window.jspdf.jsPDF
        const doc    = new jsPDF({ unit: 'mm', format: 'a4' })
        const serial = generateSerial()
        const now    = new Date()

        /* ── page 1 watermark ── */
        drawWatermark(doc)

        let y = 20

        /* ════════════ HEADER ════════════ */
        // Green header band
        doc.setFillColor(...COLORS.primary)
        doc.rect(0, 0, PAGE_W, 42, 'F')

        // Logo circle + text
        doc.setFillColor(...COLORS.white)
        doc.circle(MARGIN + 6, 21, 8, 'F')
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(9)
        doc.setTextColor(...COLORS.primary)
        doc.text('SPLaSK', MARGIN + 6, 23, { align: 'center' })

        // Title
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(20)
        doc.setTextColor(...COLORS.white)
        doc.text('Website Compliance Report', MARGIN + 20, 18)

        // Subtitle line
        doc.setFont('helvetica', 'normal')
        doc.setFontSize(9)
        doc.setTextColor(220, 240, 230)
        doc.text(`Generated: ${now.toLocaleString('en-MY', { dateStyle: 'long', timeStyle: 'short' })}`, MARGIN + 20, 26)
        doc.text(`Serial: ${serial}`, MARGIN + 20, 32)

        y = 54

        /* ════════════ SUMMARY SECTION ════════════ */
        y = drawSectionTitle(doc, y, 'Summary')

        // Summary box
        doc.setFillColor(...COLORS.light)
        doc.roundedRect(MARGIN, y, CONTENT_W, 38, 3, 3, 'F')

        const score  = Number(data.totalScore || 0)
        const status = score >= 80 ? 'PASS' : 'FAIL'
        const statusColor = score >= 80 ? COLORS.green : COLORS.red
        const scanDate = data.scanDate ? new Date(data.scanDate).toLocaleString('en-MY', { dateStyle: 'long', timeStyle: 'short' }) : '-'

        // Website URL
        doc.setFont('helvetica', 'normal')
        doc.setFontSize(9)
        doc.setTextColor(...COLORS.muted)
        doc.text('Website URL', MARGIN + 6, y + 8)
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(10)
        doc.setTextColor(...COLORS.primary)
        const displayUrl = String(data.url || '-')
        doc.text(displayUrl.length > 65 ? displayUrl.substring(0, 62) + '...' : displayUrl, MARGIN + 6, y + 14)

        // Scan date
        doc.setFont('helvetica', 'normal')
        doc.setFontSize(9)
        doc.setTextColor(...COLORS.muted)
        doc.text('Scan Date', MARGIN + 6, y + 24)
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(10)
        doc.setTextColor(...COLORS.dark)
        doc.text(scanDate, MARGIN + 6, y + 30)

        // Total score (right side, large)
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(32)
        doc.setTextColor(...statusColor)
        doc.text(`${score.toFixed(1)}%`, PAGE_W - MARGIN - 6, y + 17, { align: 'right' })

        // Status badge
        const badgeX = PAGE_W - MARGIN - 30
        const badgeY = y + 22
        doc.setFillColor(...statusColor)
        doc.roundedRect(badgeX, badgeY, 24, 9, 2, 2, 'F')
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(10)
        doc.setTextColor(...COLORS.white)
        doc.text(status, badgeX + 12, badgeY + 6.5, { align: 'center' })

        y += 46

        /* ════════════ CATEGORY SCORES ════════════ */
        y = drawDivider(doc, y)
        y = drawSectionTitle(doc, y, 'Category Scores')

        const categories = Array.isArray(data.categories) ? data.categories : []

        categories.forEach((cat) => {
            y = ensureSpace(doc, y, 14)
            const catScore = Number(cat.score || 0)
            const catColor = scoreColor(catScore)

            // Category name
            doc.setFont('helvetica', 'bold')
            doc.setFontSize(10)
            doc.setTextColor(...COLORS.dark)
            doc.text(cat.name || '', MARGIN, y)

            // Score text
            doc.setFont('helvetica', 'bold')
            doc.setFontSize(10)
            doc.setTextColor(...catColor)
            doc.text(`${catScore.toFixed(1)}%`, PAGE_W - MARGIN, y, { align: 'right' })

            y += 3

            // Progress bar
            drawProgressBar(doc, MARGIN, y, CONTENT_W - 30, 5, catScore)

            // Status badge beside score
            const catStatus = cat.status || (catScore >= 80 ? 'PASS' : 'FAIL')
            const catBadgeColor = catStatus === 'PASS' ? COLORS.green : COLORS.red
            doc.setFillColor(...catBadgeColor)
            doc.roundedRect(PAGE_W - MARGIN - 24, y - 0.5, 18, 6, 1.5, 1.5, 'F')
            doc.setFont('helvetica', 'bold')
            doc.setFontSize(7)
            doc.setTextColor(...COLORS.white)
            doc.text(catStatus, PAGE_W - MARGIN - 15, y + 3.7, { align: 'center' })

            y += 10
        })

        y += 4

        /* ════════════ DETAILED VALIDATION ════════════ */
        y = ensureSpace(doc, y, 30)
        y = drawDivider(doc, y)
        y = drawSectionTitle(doc, y, 'Detailed Validation')

        categories.forEach((cat) => {
            y = ensureSpace(doc, y, 28)
            const catScore = Number(cat.score || 0)
            const catStatus = cat.status || (catScore >= 80 ? 'PASS' : 'FAIL')
            const catBadgeColor = catStatus === 'PASS' ? COLORS.green : COLORS.red

            // Category heading row
            doc.setFillColor(...COLORS.light)
            doc.roundedRect(MARGIN, y - 4, CONTENT_W, 10, 2, 2, 'F')

            doc.setFont('helvetica', 'bold')
            doc.setFontSize(11)
            doc.setTextColor(...COLORS.primaryAlt)
            doc.text(cat.name || '', MARGIN + 4, y + 2)

            doc.setFont('helvetica', 'bold')
            doc.setFontSize(10)
            doc.setTextColor(...scoreColor(catScore))
            doc.text(`${catScore.toFixed(1)}%`, PAGE_W - MARGIN - 30, y + 2, { align: 'right' })

            // Status badge
            doc.setFillColor(...catBadgeColor)
            doc.roundedRect(PAGE_W - MARGIN - 22, y - 2.5, 18, 7, 1.5, 1.5, 'F')
            doc.setFont('helvetica', 'bold')
            doc.setFontSize(7.5)
            doc.setTextColor(...COLORS.white)
            doc.text(catStatus, PAGE_W - MARGIN - 13, y + 2, { align: 'center' })

            y += 10

            // Explanation
            if(cat.explanation){
                y = ensureSpace(doc, y, 8)
                doc.setFont('helvetica', 'italic')
                doc.setFontSize(8.5)
                doc.setTextColor(...COLORS.muted)
                const lines = doc.splitTextToSize(cat.explanation, CONTENT_W - 8)
                doc.text(lines, MARGIN + 4, y)
                y += lines.length * 4 + 3
            }

            // Sub-categories table
            const subs = Array.isArray(cat.subCategories) ? cat.subCategories : []
            if(subs.length){
                const tableData = subs.map((sub) => [
                    sub.name || '',
                    sub.status || 'FAIL',
                    sub.explanation || 'No details'
                ])

                y = ensureSpace(doc, y, 10 + subs.length * 8)

                doc.autoTable({
                    startY: y,
                    margin: { left: MARGIN + 2, right: MARGIN + 2 },
                    head: [['Sub-Category', 'Status', 'Details']],
                    body: tableData,
                    theme: 'grid',
                    styles: {
                        fontSize: 8,
                        cellPadding: 2.5,
                        lineColor: [220, 220, 220],
                        lineWidth: 0.25,
                        textColor: COLORS.dark
                    },
                    headStyles: {
                        fillColor: COLORS.primary,
                        textColor: COLORS.white,
                        fontStyle: 'bold',
                        fontSize: 8.5
                    },
                    columnStyles: {
                        0: { cellWidth: 40, fontStyle: 'bold' },
                        1: { cellWidth: 18, halign: 'center' },
                        2: { cellWidth: 'auto' }
                    },
                    bodyStyles: {
                        minCellHeight: 7
                    },
                    didParseCell: (hookData) => {
                        if(hookData.section === 'body' && hookData.column.index === 1){
                            const val = hookData.cell.raw
                            hookData.cell.styles.textColor = val === 'PASS' ? COLORS.green : COLORS.red
                            hookData.cell.styles.fontStyle = 'bold'
                        }
                    },
                    didDrawPage: () => {
                        drawWatermark(doc)
                    }
                })

                y = doc.lastAutoTable.finalY + 8
            } else {
                doc.setFont('helvetica', 'italic')
                doc.setFontSize(8)
                doc.setTextColor(...COLORS.muted)
                doc.text('No sub-category details available.', MARGIN + 4, y)
                y += 8
            }
        })

        /* ── footers on every page ── */
        drawFooter(doc, serial)

        /* ── download ── */
        const safeName = (data.url || 'website').replace(/https?:\/\//, '').replace(/[^a-zA-Z0-9.-]/g, '_').substring(0, 40)
        doc.save(`SPLaSK_Report_${safeName}_${serial}.pdf`)

        return serial
    }

    /* ── public API ── */
    return { generateReport, generateSerial }
})()
